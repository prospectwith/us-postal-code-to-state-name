const data = require("./data.json");

/**************************************************************************
 * DATA
 ***************************************************************************/

module.exports.data = data;

/**************************************************************************
 * METHODS
 ***************************************************************************/

module.exports.convertPostalCodeToStateName = function (postalCode) {
  postalCode = postalCode.toLowerCase();

  if (data[postalCode]) {
    return data[postalCode].name;
  }

  return null;
};
